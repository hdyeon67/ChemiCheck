/** 두 오행의 케미 반응 문구 (순서 무관) */
export function reactionFor(a: Ohaeng, b: Ohaeng): string {
  return REACTIONS[pairKey(a, b)] ?? "서로 다른 기운이 만나 묘하게 끌리는 케미 ✨";
}

/** 전체 반응 문구 (검증용) */
