// 두 캐릭터(오행)의 "케미 반응" 문구 — 왜 이 케미인지 위트있게 설명.
//   궁합은 대칭이므로 정렬된 오행 쌍(15종)을 키로 사용해 입력 순서와 무관하게 동일.

import type { Ohaeng } from "../scoring/types";

const ORDER: Ohaeng[] = ["목", "화", "토", "금", "수"];

function pairKey(a: Ohaeng, b: Ohaeng): string {
  return [a, b].sort((x, y) => ORDER.indexOf(x) - ORDER.indexOf(y)).join("");
}

